   private void switchCard(String card) {
      CardLayout c = (CardLayout) panelFeeInfo.getLayout();
      c.show(panelFeeInfo, card);
      if ("add".equals(card))
      {
         this.txtDescriptionAdd.setText("");
         this.txtNameAdd.setText("");
         this.txtRateAdd.setText("");
         chkTaxAdd.setSelected(true);
      }
   }

   private void populateFields() {
      this.lblCreatedBy.setText(fee.getCreated_by().toString());
      this.lblDateCreated.setText(fee.getDate_created().toString());
      this.lblDescription.setText(fee.getDescription());
      this.lblName.setText(fee.getName());
      this.lblRate.setText(fee.getRateString());
      
      this.txtDescription.setText(fee.getDescription());
      this.txtName.setText(fee.getName());
      this.txtRate.setText(String.valueOf(fee.getRate()));

      chkHasTax.setSelected(fee.isTax());
      chkTax.setSelected(fee.isTax());
   }

   private void setFee(Fee fee) {
      this.fee = fee;
      populateFields();
   }
   private void errorMsg(String message) {
      lblMsg.setForeground(new Color(255,102,102));
      lblMsg.setText(message);
   }
   private void Msg(String message)
   {
      lblMsg.setForeground(new Color(0,204,51));
      lblMsg.setText(message);
   }

   private Fee getEditFields() {
      Fee fee = new Fee();
      fee.setName(txtName.getText().trim());
      fee.setDescription(txtDescription.getText().trim());
      fee.setRate(Float.valueOf(txtRate.getText()));
      fee.setTax(chkTax.isSelected());
      return fee;
   }
   
   private Fee getAddFields(){
      Fee fee = new Fee();
      fee.setName(txtNameAdd.getText().trim());
      fee.setDescription(txtDescriptionAdd.getText().trim());
      fee.setRate(Float.valueOf(txtRateAdd.getText()));
      fee.setTax(chkTaxAdd.isSelected());
      return fee;
   }
