   @Override
   public void refresh() {
      chkTaxAdd.setSelected(true);
   
      feeModel.clear();
      
      List<Fee> fees;
      try {
         fees = FinanceSetting.loadAllFees();
         for (Fee f: fees)
      {
         feeModel.addElement(f);
      }
      } catch (DatabaseException ex) {
         PopupMessage.createErrorPopUp(ex.getMessage(), null);
      }
      ;
      try {
         txtTax.setText(String.valueOf(FinanceSetting.getTax()));
      } catch (DatabaseException ex) {
         PopupMessage.createErrorPopUp(ex.getMessage(), null);
      }
      
      if (feeModel.size() > 0)
      {
         lstFees.setSelectedIndex(0);
      }else
      {
         btnEdit.setEnabled(false);
         btnDelete.setEnabled(false);
      }
      
      
   }

